# list
x = ["apple", "banana", "cherry"]
print(x)
print(type(x)) 

#tuple
x = ("apple", "banana", "cherry")
print(x)
print(type(x)) 

#dict
x = {"name" : "John", "age" : 36}
print(x)
print(type(x)) 

#set
x = {"apple", "banana", "cherry"}
print(x)
print(type(x))

#frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x))

